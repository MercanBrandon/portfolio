
 <h2 class="titre">Une pré-5G pour 2018 ! </h2>
<div>
 <p class="dateArticle"> Le 30 mars 17 </p>
 <p class="content">Tout comme avec la 4G avec le dual-carrier ou H+, la 5G pourrais avoir une norme transitoire, une sorte de pré-5G. <br/>
Selon Mérouane Debbah, directeur de la R&D chez Huawei France explique à nos confrères des Echos : <br/>
Il s’agit en fait d’une pré-5G, qui fonctionnera sur les réseaux actuels. On va pouvoir prendre les infrastructures et les fréquences utilisées par la 4G pour fournir des performances qui ressemblent à certaines promesses de la 5G.
</p>

  <p>Lors de ce second stage le projet  consistait en la production d’une application de gestion pour une association local de danse traditionnel, j’ai nommé SAKITAW.</br> Cette application devait prendre en charge à la fois les membre et les professeurs mais aussi comporter tout un volet financier. Pour ce projet j’ai été accompagné de deux de mes camarades de classe : Mlle BLECOURT Alice et Mr CHARLES Logan. </p>
  <p>Nous avons commencé par une expression de besoins en nous déplaçant directement au sein de l’association avant de d’entamer la conception de la base de données avant l’interface de l’application et enfin l’application en elle-même que nous n’avons malheureusement pas encore terminé.</p>
